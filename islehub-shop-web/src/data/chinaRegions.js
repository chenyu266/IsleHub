function option(label, children) {
  const item = { label, value: label }
  if (children?.length) item.children = children
  return item
}

function fromOverride(provinceName, rows) {
  return option(provinceName, rows.map(([cityName, districts]) =>
    option(cityName, districts.map(districtName => option(districtName)))
  ))
}

function fromAreaData(provinceCode, provinceName) {
  const cities = areaData[provinceCode]
  if (!cities) {
    return option(provinceName, [option(provinceName, [option(provinceName)])])
  }

  return option(provinceName, Object.entries(cities).map(([cityCode, cityName]) => {
    const districts = areaData[cityCode]
    const districtOptions = districts
      ? Object.values(districts).map(districtName => option(districtName))
      : [option(cityName)]
    return option(cityName, districtOptions)
  }))
}

export const chinaRegionOptions = Object.entries(areaData[REGION_ROOT_CODE]).map(([provinceCode, provinceName]) => {
  const override = SPECIAL_REGION_OVERRIDES[provinceCode]
  return override
    ? fromOverride(provinceName, override)
    : fromAreaData(provinceCode, provinceName)
})

export function isValidRegionPath(path) {
  if (!Array.isArray(path) || path.length !== 3) return false
  let options = chinaRegionOptions
  for (const value of path) {
    const matched = options.find(item => item.value === value)
    if (!matched) return false
    options = matched.children || []
  }
  return true
}
